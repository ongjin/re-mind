COMMENT ON COLUMN "SELF_CHECK_RESULT"."RESULT_NO" IS '증상 결과 번호';

COMMENT ON COLUMN "SELF_CHECK_RESULT"."CATEGORY_NO" IS '증상 카테고리 번호';

